        {
            validar();
            obtenerDatos();
            calcPerimetro();
            calcApotema();
            calcArea();
            mostrarDatos();
        }
        else
        {
            int resp = JOptionPane.showOptionDialog(null,
            "Â¿Desea comprobar otro poligono?",
            null, JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,
            null, null, null);
            
            if (resp == 0)
            {
                limpiar ();
            }
            else
            {
                System.exit(0);
            }
        }
