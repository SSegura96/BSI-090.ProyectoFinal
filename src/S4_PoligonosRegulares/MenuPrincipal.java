    public void validar () throws NumberFormatException
    {
        try
        {

            numLados = Integer.parseInt(jTNumLados.getText());

            valorLado = Integer.parseInt(jTValorLado.getText());

            if (numLados <= 0 )
            {
               error = 1;
               jTNumLados.setText("Numero no valido");
               throw new NumberFormatException();
            }

            if (valorLado <= 0)
            {
               error = 1;
               jTValorLado.setText("Numero no valido");
               throw new NumberFormatException();
            }
        }
        catch (NumberFormatException e)
        {    
            mensajeError = "El numero ingresado no es valido"; 
        
            JOptionPane.showMessageDialog(null, mensajeError);
        }
    }
    
    public void mensaje ()
    {
        JOptionPane.showMessageDialog(null, "Bienvenido al sistema de calculo "
                                          + "\nde area de poligonos regulares");
    }
    
    public void obtenerDatos ()
    {
        numLados = Integer.parseInt(jTNumLados.getText());
        valorLado = Double.parseDouble(jTValorLado.getText());
    }
            
    public void calcPerimetro ()
    {
        perimetro = numLados * valorLado;
    }
    
    public void calcApotema ()
    {
        apotema = (valorLado)/2*Math.tan(Math.toRadians(180/numLados));
    }
    
    public void calcArea ()
    {
        area = (apotema*perimetro)/2;
    }
    
    public void mostrarDatos ()
    {
        jLTitulo.setText("Resultado");
        jLSub1.setText("Area");
        jLSub2.setText("Perimetro");
        
        jTNumLados.setText(area+"");
        jTValorLado.setText(perimetro+"");
        
        jBCalcular.setText("Listo");
        
        tipo = 1;
    }
    
    public void limpiar ()
    {
        jLTitulo.setText("Introduzca los datos");
        jLSub1.setText("Numero de lados");
        jLSub2.setText("Valor del Lado");
        
        jTNumLados.setText("1,2,3,4,5");
        jTValorLado.setText("1,2,3,4,5");
        
        jBCalcular.setText("Calcular");
        
        tipo = 0;
    }
    
