   public void elementos ()
   {
       colocarlogo(main.nombreFigura);
       
       if(main.elementoSelect == 0)
       {
            Linea lin = new Linea(dato1);
            lin.calcArea();
            lin.calcPerimetro();
            jTArea.setText(lin.area+"");
            jTPerimetro.setText(lin.perimetro+"");
       }
       else
       {
            if (main.elementoSelect == 1)
            {
                Cuadrado cua = new Cuadrado (dato1);
                cua.calcArea();
                cua.calcPerimetro();
                jTArea.setText(cua.area+"");
                jTPerimetro.setText(cua.perimetro+"");
            }
            else
            {
                Rectangulo rec = new Rectangulo(dato1, dato2);
                rec.calcArea();
                rec.calcPerimetro();
                jTArea.setText(rec.area+"");
                jTPerimetro.setText(rec.perimetro+"");
            }
       }
      
   }
    
   public void configurar ()
    {
        if (main.selecArea == true)
        {
            jTPerimetro.setText(" - ");
            jTPerimetro.setEditable(false);
        }
        else
        {
            if (main.selecPerimetro)
            {
                jTArea.setText(" - ");
                jTArea.setEditable(false);
            }
        }         
    }
    
    public void colocarlogo (String figura)
    {
        jLLogo.setIcon(new ImageIcon("src/imgs/"+figura+".png"));
    }
    
    public void obtenerFigura ()
    {
       jLNombreFigura.setText(main.nombreFigura);
    }
    
